package PracticeSet;

public class SwapNumber {
	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		int c;
		
		System.out.println( "Before Swapping Numbers======>"+a+" "+ b );
		
		// swapping of numbers
		
		c=a;
		a=b;
		b=c;
		
		System.out.println("After swapping numbers====> "+a+" "+c);
		System.out.println();
		
		
		
		
		
	}

}
